    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(this.experiencia.getText().equals("") || capacitado.getSelectedIndex()==0 || id.getText().equals("") || nombre1.getText().equals("") || nombre2.getText().equals("") || apellido1.getText().equals("") || apellido2.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Complete todos los datos!");
        }else{
            leerNuevoDetective();
            registrarCaso();
            txt1.textDetectives(modelo);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    public void registrarCaso() {
        Persona nuevoDetective = this.leerNuevoDetective();
        this.modelo.insertarDetectives(nuevoDetective);
    }

    public Persona leerNuevoDetective() {

        byte experiencia = Byte.parseByte(this.experiencia.getText());
        String capacitado = String.valueOf(this.capacitado.getSelectedItem());
        String id =this.id.getText();
        String nombre1 = this.nombre1.getText();
        String nombre2 = this.nombre2.getText();
        String apellido1 = this.apellido1.getText();
        String apellido2 = this.apellido2.getText();
        persona = new Detective(experiencia, capacitado, id, nombre1, nombre2, apellido1,apellido2);

        return persona;
    }

